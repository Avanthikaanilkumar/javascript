let aw=" JavaScript is Awesome! ";

console.log(" Length of the string:", aw.length);

console.log(" Character at index 5:", aw.charAt(5));

console.log(" Unicode value at index 5:", aw.charCodeAt(5));

console.log(" Last character:", aw.charAt(aw.length - 1));

console.log(" Character at index 3:", aw.charAt(3));

console.log(" Uppercase:", aw.toUpperCase());

console.log(" lowercase:", aw.toLowerCase());

console.log(" Concatenated string:", aw.concat(" Let's Learn!"));

console.log(" Trim start:", aw.trimStart());

console.log(" Trim End:", aw.trimEnd());

console.log(" Repeated string:", aw.repeat(2));

console.log(" Replace 'Awesome' with 'Cool':", aw.replace("Awesome", "Cool"));

console.log(" Replace spaces with '-':", aw.replaceAll(" ", "-"));


console.log(" Array of words:", aw.trim().split(" "));

console.log(" Trimmed string:", aw.trim());





